const db = require ('../config/db');

//-------------------------------------------------------------------------------------------------------------
const getAllProveedores = async() =>{
    const [result] = await db.query('SELECT * FROM proveedores');
    return result;
};

//-------------------------------------------------------------------------------------------------------------
const getProveedorPorId = async(id) => {
    const[result] = await db.query('SELECT * FROM proveedores WHERE id_proveedor = ?', [id]);
    return result;
};

//-------------------------------------------------------------------------------------------------------------
const newProveedor =  async(nom, dir, email, tel) => {
    const [result] = await db.query(
    'INSERT INTO proveedores (nombre, direccion,email,telefono) VALUES(?,?,?,?)',
        [nom,dir,email,tel]);
    return result;
};
//-------------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------------



module.exports = {
    getAllProveedores,
    getProveedorPorId,
    newProveedor
};
